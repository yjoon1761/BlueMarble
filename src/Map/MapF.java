package Map;

public class MapF {
    private String name;
    private String type;
    // 다른 필요한 속성 추가

    public MapF(String name, String type) {
        this.name = name;
        this.type = type;
    }

    // Getter 메서드 추가
    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}

